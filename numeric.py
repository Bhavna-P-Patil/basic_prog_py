#Numeric data type


no=11             #int

print("value of no is :",no)

print("Data type of no is :",type(no))

print("unique ID of no is :",id(no))


value=11.454       #float

print("value of no is :",value)

print("Data type of no is :",type(value))

print("unique ID of no is :",id(value))


a=4+5j          #complex

print("value of no is :",a)

print("Data type of no is :",type(a))

print("unique ID of no is :",id(a))