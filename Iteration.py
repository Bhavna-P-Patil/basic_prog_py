

# Seqence approach



def StartS():
    print("Jay Ganesh")
    print("Jay Ganesh")
    print("Jay Ganesh")
    print("Jay Ganesh")
    print("Jay Ganesh")


# Iteration approach
def Start():
    iCnt =0
    while iCnt<5:
        print("Jay Ganesh")
        iCnt = iCnt + 1


    print("Jay Ganesh")

def main():
    print("Output by sequence")
    StartS()
    print("Output by Iteration")
    Start()


if __name__=="__main__":
    main()
